int	ft_sqrt(int nb)
{
	unsigned	i;
	unsigned	n;

	n = nb;
	if (nb < 0 || nb > 2147483647)
		return (0);
	i = 2;
	while (n >= i * i)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (0);
}
